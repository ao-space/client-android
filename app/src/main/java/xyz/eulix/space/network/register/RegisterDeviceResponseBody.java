package xyz.eulix.space.network.register;

import xyz.eulix.space.interfaces.EulixKeep;

/**
 * @author: chenjiawei
 * date: 2021/6/16 18:15
 */
public class RegisterDeviceResponseBody implements EulixKeep {
    private int code;
    private String data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    @Override
    public String toString() {
        return "RegisterDeviceResponseBody{" +
                "code=" + code +
                ", data='" + data + '\'' +
                '}';
    }
}
