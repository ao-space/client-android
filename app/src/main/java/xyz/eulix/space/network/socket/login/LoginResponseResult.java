package xyz.eulix.space.network.socket.login;

import xyz.eulix.space.interfaces.EulixKeep;

/**
 * @author: chenjiawei
 * date: 2021/6/21 16:29
 */
public class LoginResponseResult implements EulixKeep {
    private int code;
    private String msg;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    @Override
    public String toString() {
        return "LoginResponseResult{" +
                "code=" + code +
                ", msg='" + msg + '\'' +
                '}';
    }
}
